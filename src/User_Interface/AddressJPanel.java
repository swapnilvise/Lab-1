    private void lbl_SaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lbl_SaveActionPerformed
        // TODO add your handling code here:
        
        //String regex =  "^[0-9]{5}(?:-[0-9]{4})?$";
        errorcode_ZIPCode = Pattern.matches("\\d{5}",txt_Zipcode.getText());
        
        if (txt_Street_Name.getText().isBlank() || txt_City.getText().isBlank() || txt_Zipcode.getText().isBlank() || drpdwn_State.getSelectedItem().equals("Select") || drpdwn_Country.getSelectedItem().equals("Select"))
            JOptionPane.showMessageDialog(this, "Mandatory fields cannot be blank.");
        else if (errorcode_ZIPCode == false)
            JOptionPane.showMessageDialog(this, "ZIP Code should be Numeric.");
        else
        { product.setStreet_Name(txt_Street_Name.getText());
            product.setApartment_Number(txt_ApartmentNo.getText());
            product.setCity(txt_City.getText());
            product.setState(drpdwn_State.getSelectedItem().toString());
            product.setZIP_Code(txt_Zipcode.getText());
            product.setCountry(drpdwn_Country.getSelectedItem().toString());
            JOptionPane.showMessageDialog(this, "Address has been Saved.");
        }
    }//GEN-LAST:event_lbl_SaveActionPerformed


